// Same-origin in production (the backend serves the built frontend); the dev
// server talks to the API on :8000. Override with VITE_API_URL.
const API_BASE: string =
  (import.meta.env.VITE_API_URL as string | undefined) ??
  (import.meta.env.DEV ? "http://localhost:8000" : "");

// Optional API key (generated by ./setup.sh). Sent on every request as a header.
const API_KEY: string | undefined = import.meta.env.VITE_API_KEY as string | undefined;

const apiUrl = (path: string) => `${API_BASE}/api${path}`;

function authHeaders(base: Record<string, string> = {}): Record<string, string> {
  return API_KEY ? { ...base, "X-API-Key": API_KEY } : base;
}

// True only in the browser. During SSR/SPA-shell prerender there is no backend,
// so network calls are skipped (queries stay pending → the static shell renders
// its loading state; the client fetches for real after hydration).
const IS_BROWSER = typeof window !== "undefined";

async function http<T>(path: string, init?: RequestInit): Promise<T> {
  if (!IS_BROWSER) return new Promise<T>(() => {}); // never resolves during prerender
  const isJson = init?.body && !(init.body instanceof FormData);
  const res = await fetch(apiUrl(path), {
    ...init,
    headers: authHeaders(isJson ? { "content-type": "application/json" } : {}),
  });
  if (!res.ok) {
    throw new Error(`${res.status} ${res.statusText}`);
  }
  if (res.status === 204) return undefined as T;
  return (await res.json()) as T;
}

// Shared fetch-based SSE reader (works for GET and POST, and carries the auth
// header — which EventSource cannot). Returns an unsubscribe function.
function streamSSE(path: string, init: RequestInit, onEvent: (event: unknown) => void): () => void {
  if (!IS_BROWSER) return () => {};
  const ctrl = new AbortController();
  (async () => {
    const res = await fetch(apiUrl(path), {
      ...init,
      headers: authHeaders(init.headers as Record<string, string>),
      signal: ctrl.signal,
    });
    if (!res.body) return;
    const reader = res.body.getReader();
    const decoder = new TextDecoder();
    let buffer = "";
    for (;;) {
      const { done, value } = await reader.read();
      if (done) break;
      buffer += decoder.decode(value, { stream: true });
      let idx: number;
      while ((idx = buffer.indexOf("\n\n")) >= 0) {
        const frame = buffer.slice(0, idx);
        buffer = buffer.slice(idx + 2);
        const dataLine = frame.split("\n").find((l) => l.startsWith("data:"));
        if (dataLine) {
          try {
            onEvent(JSON.parse(dataLine.slice(5).trim()));
          } catch {
            /* ignore malformed frame */
          }
        }
      }
    }
  })().catch(() => {
    /* aborted or network error */
  });
  return () => ctrl.abort();
}

// ---------------- Projects ----------------
export function listProjects(): Promise<Project[]> {
  return http<Project[]>("/projects");
}

export async function getProject(id: string): Promise<Project | undefined> {
  try {
    return await http<Project>(`/projects/${id}`);
  } catch {
    return undefined;
  }
}

export function createProject(input: {
  file: File;
  audience: Project["audience"];
  aspectRatio: Project["aspectRatio"];
  language: string;
  targetLengthMin: number;
}): Promise<Project> {
  const form = new FormData();
  form.append("file", input.file);
  form.append("audience", input.audience);
  form.append("aspectRatio", input.aspectRatio);
  form.append("language", input.language);
  form.append("targetLengthMin", String(input.targetLengthMin));
  return http<Project>("/projects", { method: "POST", body: form });
}

